class OddEven {

    public void checkOddEven(int num) {

        // Comprobamos si el número es par ('Si' el resto de la división 
        // de num por dos da cero, es par).
        if(num%2==0) {

            // Si es par, muestra por pantalla número par.
            System.out.println("Número par");

        } else {

            // SI no es par, muestra por pantalla número impar.
            System.out.println("Número impar");

        }
        
    }

}